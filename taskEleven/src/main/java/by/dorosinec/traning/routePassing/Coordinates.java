public class Coordinates {
    private double coordinateX;
    private double coordinateY;

    public Coordinates(double X, double Y) {
        this.coordinateX = X;
        this.coordinateY = Y;
    }

    public double getCoordinateX() {
        return coordinateX;
    }

    public double getCoordinateY() {
        return coordinateY;
    }
}
